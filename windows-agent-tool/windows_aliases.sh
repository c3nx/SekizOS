#!/bin/bash
# Windows Control Aliases for easy access

# Screenshot
alias winshot='win screenshot'
alias winscreenshot='win screenshot'

# Mouse control
alias winclick='win click'
alias winmove='win move'

# Keyboard
alias wintype='win type'
alias winkey='win key'

# PowerShell
alias winps='win ps'
alias winpowershell='win powershell'

# Process management
alias winprocs='win processes'
alias winkill='win kill'

# File operations
alias winread='win read'
alias winwrite='win write'

# Common shortcuts
alias wincmd='win ps cmd /c'
alias windir='win ps "Get-ChildItem"'
alias windate='win ps "Get-Date"'

# Quick actions
alias winenter='win key enter'
alias winesc='win key escape'
alias wintab='win key tab'

echo "Windows control aliases loaded!"